public class class14 {

    public static void main(String args[]){
        //Math is an builtin class in java , which has all the math operations
        //absolute value
        System.out.println(Math.abs(-24.8));
        //rounding number in ceiling
        System.out.println(Math.ceil(14.8));
        //rounding number floor
        System.out.println(Math.floor(14.8));
        //finding max
        System.out.println(Math.max(20,30));
        //finding min
        System.out.println(Math.min(20,30));
        //power method 1st paremeter is the value, 2nd paremeter is the power (2^3) --->  (2,3) = 8
        System.out.println(Math.pow(2,3));
        //square root method
        System.out.println(Math.sqrt(16));

    }
}
